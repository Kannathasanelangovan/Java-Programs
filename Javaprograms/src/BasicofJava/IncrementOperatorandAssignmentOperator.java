package BasicofJava;

public class IncrementOperatorandAssignmentOperator {
	
	public static void main(StringConcept[] args) {
		
		//Post Increment
		
		int x = 10;
		
		System.out.println(x++);
		System.out.println(x);
		
		//PreIncrement
		
		System.out.println(++x);
		System.out.println(x);
		
		x+=5; //x =x+5; same for x =x*5;
		
		System.out.println(x);
		
		
		

	}

}
