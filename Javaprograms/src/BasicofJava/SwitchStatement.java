package BasicofJava;

public class SwitchStatement {

	public static void main(StringConcept[] args) {
		
		int score = 70;
		
		//byte,short,int,char
		
		switch(score)
		
		{
		
		case 90:
			
			System.out.println("Very Good");
			break;
		
		case 80:
			
			System.out.println("Good");
			break;
			
			default:
				
				System.out.println("Average");
				break;
		}

		
		
	}

}
