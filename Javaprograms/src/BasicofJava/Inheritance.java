package BasicofJava;

public class Inheritance {
	
	/*
	 * Classes in java can be extended
	 * creating new classes which retain characteristics of base class
	 *
	 * 
	 * Is - A relation ship
	 */

	protected int height;
	protected int width;
	

	public void set_values(int a,int b){
		
		height = a;
		width = b;
		
	}
}
